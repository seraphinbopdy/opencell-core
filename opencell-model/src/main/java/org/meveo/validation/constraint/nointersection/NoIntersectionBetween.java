package org.meveo.validation.constraint.nointersection;

import javax.validation.Constraint;
import javax.validation.Payload;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.*;
import static java.lang.annotation.RetentionPolicy.RUNTIME;


@Constraint(validatedBy = NoIntersectionValidator.class)
@Target({TYPE})
@Retention(RUNTIME)
public @interface NoIntersectionBetween {

	String message() default "Collections {firstCollection} and {secondCollection} should not intersect";
	Class<?>[] groups() default {};
	Class<? extends Payload>[] payload() default {};

	String firstCollection();
	String secondCollection();

}
